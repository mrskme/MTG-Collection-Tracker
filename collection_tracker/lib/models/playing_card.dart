class PlayingCard {
  late String? name;
  late List<String>? names;
  late String? manaCost;
  late int? cmc;
  var variations;
  var watermark;
  var border;
  var timeShifted;
  var hand;
  var reserved;
  var releaseDate;
  var starter;
  var rulings;
  late List<String>? colors;
  late List<String>? colorIdentity;
  late String? type;
  late List<String>? supertypes;
  late List<String>? types;
  late List<String>? subtypes;
  late String? rarity;
  late String? set;
  late String? setName;
  late String? text;
  late String? flavor;
  late String? artist;
  late String? number;
  late String? power;
  late String? toughness;
  late int? loyality;
  late List<String> gameFormats;
